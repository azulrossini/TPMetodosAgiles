    private void botonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActualizarActionPerformed
        //Valido los datos  ingresados y dependiendo del resultado muestro el mensaje correspondiente
        switch (personaController.validarDatosMT(this.jTextFieldNombre.getText(), this.jTextFieldApellido.getText(), this.jTextFieldCalle.getText() ,this.jTextFieldNumeroCalle.getText(), this.jTextFieldPiso.getText(), this.jTextFieldDepartamento.getText())){
            case 0:
            try {
                //Actualizo la persona existente
                persona.setNombre(this.jTextFieldNombre.getText());
                persona.setApellido(this.jTextFieldApellido.getText());
                this.calle=this.jTextFieldCalle.getText();
                this.numero=this.jTextFieldNumeroCalle.getText();
                this.piso=this.jTextFieldPiso.getText();
                this.dpto=this.jTextFieldDepartamento.getText();
                
                persona.setDomicilio(calle+" "+numero+" "+piso+" "+dpto);
                persona.setDonante(jRadioButtonDonanteSi.isSelected());
                //Actualizo en la BD
                personaController.updateTitular(persona);
                //Muestro mensaje de exito
                JOptionPane.showMessageDialog(this, "Ha actualizado el titular con √©xito", "Felicidades",  JOptionPane.INFORMATION_MESSAGE);
                //Vuelvo a la pantalla de buscar titular
                Index.historial.get(Index.historial.size()-2).setVisible(true);
                this.dispose();
                Index.historial.remove(Index.historial.size()-1);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "La contrase√±a no es v√°lida", "Error",  JOptionPane.ERROR_MESSAGE);
            }
            break;
            case 1:
            JOptionPane.showMessageDialog(this, "El nombre no es correcto", "Error",  JOptionPane.ERROR_MESSAGE);
            break;
            case 2:
            JOptionPane.showMessageDialog(this, "El apellido no es correcto", "Error",  JOptionPane.ERROR_MESSAGE);
            break;
            case 3:
            JOptionPane.showMessageDialog(this, "La calle insertada no es correcto o la extensi√≥n del domicilio supera el l√≠mite(32)", "Error",  JOptionPane.ERROR_MESSAGE);
            break;
            case 4:
            JOptionPane.showMessageDialog(this, "El n√∫mero de calle insertado no es v√°lido", "Error",  JOptionPane.ERROR_MESSAGE);
            break;
            case 5:
            JOptionPane.showMessageDialog(this, "El n√∫mero de piso insertado no es v√°lido", "Error",  JOptionPane.ERROR_MESSAGE);
            break;
            case 6:
            JOptionPane.showMessageDialog(this, "El departamento insertado no es v√°lido", "Error",  JOptionPane.ERROR_MESSAGE);
            break;
        }
    }//GEN-LAST:event_botonActualizarActionPerformed

