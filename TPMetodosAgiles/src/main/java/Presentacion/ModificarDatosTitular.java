    //Completo la interfaz con los datos del titular que fue elegido
    private void cargarCampos(){
        this.jLabelTipoID.setText(persona.getTipoId());
        this.jLabelID.setText(persona.getNroId());
        this.jTextFieldNombre.setText(persona.getNombre());
        this.jTextFieldApellido.setText(persona.getApellido());
        this.jLabelFechaN.setText(persona.getFechaNac().toString());
        
        //Creo un tokenizer para obtener los datos del domicilio por separado y as√≠ poder modificarlos y/o validarlos
        String domicilio = persona.getDomicilio();
        StringTokenizer st = new StringTokenizer(domicilio);
        
        if (st.hasMoreTokens()){
            this.calle=st.nextToken();
            this.jTextFieldCalle.setText(calle);
        }
        if (st.hasMoreTokens()){
            this.numero= st.nextToken();
            this.jTextFieldNumeroCalle.setText(numero);
        }
        if (st.hasMoreTokens()){
            this.piso= st.nextToken();
            this.jTextFieldPiso.setText(piso);
        }
        if (st.hasMoreTokens()){
            this.dpto= st.nextToken();
            this.jTextFieldDepartamento.setText(dpto);
        }
        if (this.persona.isDonante()){
            this.jRadioButtonDonanteSi.setSelected(true);
        }
        else{
            this.jRadioButtonDonanteNo.setSelected(true);
        }
    }

